class TeaStall{
	static int teaStallId;
	static String name;
	static String address;
	static long contactNo;
	
	static void setTeaStallId(int id)
	{
		teaStallId = id;
	}
    static int getTeaStallId()
	{
	return teaStallId;
    }
    static void setContactNo(long no)
    {
	 contactNo = no;
	}
	static long getContactNo()
	{
	return contactNo;
	}
    static void setName(String tname)
	{
		name = tname;
	}
	static String getName()
	{
	return name;
	}
	
	static void setAddress(String taddress)
	{
		address = taddress;
	}
	static String getAddress()
	{
	return address;
	}
	
	
	
}


